package javavariable;

public class variables1 

{

public static void main(String[] args)

{
   //1.variable declaration (allocating/reserving memory)
	
	String studentname;   //data type variable name 
	String studentdept;
	float studentper;
	int studentRollNum;
	char studentGrade;
	
	//2.variable initialization (Assigning/inserting value)
	
	studentname = "bhushan";
	studentdept = "civil";
	studentper = 82.20f;
	studentRollNum = 202;
	studentGrade = 'B';
	
	//3.variable usage 
	
	System.out.println(studentname);
	System.out.println(studentdept);
	System.out.println(studentper+"%");
	System.out.println(studentRollNum);
	System.out.println(studentGrade);

			
	
}	
	
}
