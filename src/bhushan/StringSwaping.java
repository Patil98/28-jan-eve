package bhushan;

public class StringSwaping 
{

	public static void main(String[] args)
	{
		String s1="bhushan";
		String s2="patil";
		String s3;
		
		System.out.println("before swaping :"+s1+"  "+s2);
		
	      s3=s1;
	      s1=s2;
	      s2=s3;
	      
	   System.out.println("After swaping :"+s1+"  "+s2);
	    		  	
	}
	
}
